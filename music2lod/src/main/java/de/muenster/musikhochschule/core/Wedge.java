package de.muenster.musikhochschule.core;

public class Wedge {

	private String type;
	private int number;
	
	public Wedge() {
		super();
	}
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getSpread() {
		return number;
	}
	public void setSpread(int number) {
		this.number = number;
	}
	
}
