package de.muenster.musikhochschule.core;

public class Slur {

	private int number;
	private String type;
	
	public Slur() {
		super();
	}
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	
}
