package de.muenster.musikhochschule.core;

public class Creator {

	private String name;
	private String type;
	
	public Creator() {
	
		super();
	
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
