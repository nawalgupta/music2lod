package de.muenster.musikhochschule.core;

public class Dynamic {
	
	private String type;
	private String value;
	
	public Dynamic() {

		super();

	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	
}

