package de.muenster.musikhochschule.core;

public class Work {

	String title;
	String number;
	
	public Work() {
		super();
	
	}
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
	
}
