package de.ulb.musik.core;

public class Lyric {

	String syllabic;
	String text;
	
	public Lyric() {
		super();
	
	}

	public String getSyllabic() {
		return syllabic;
	}

	public void setSyllabic(String syllabic) {
		this.syllabic = syllabic;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
	
	
}
